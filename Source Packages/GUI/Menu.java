    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        
        Uposlenik u = new Uposlenik();
        u.setVisible(true);
        ResultSet rs = DB.getAmbulante();
        try {
            while(rs.next())
            {
                u.UposAmb.addItem(rs.getString("naziv"));
               
            }
        } catch (SQLException ex) {
            Logger.getLogger(Uposlenik.class.getName()).log(Level.SEVERE, null, ex);
            System.err.println(ex.getMessage());
        }
        
        ResultSet rs2 = DB.getUloge();
        try {
            while(rs2.next())
            {
                u.UposUloga.addItem(rs2.getString("naziv"));
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(Uposlenik.class.getName()).log(Level.SEVERE, null, ex);
            System.err.println(ex.getMessage());
        }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

